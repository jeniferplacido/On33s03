# Define uma variável 'nota' e atribui o valor 5 a ela
nota = 5
# Define uma variável 'presenca' e atribui o valor 9 a ela
presenca = 9

# Verifica se a 'nota' é maior ou igual a 7 e se a 'presenca' é maior ou igual a 8
if nota >= 7 and presenca >= 8:
    # Se ambas as condições forem verdadeiras, imprime 'Aprovada' no console
    print('Aprovada')
else:
    # Se qualquer uma das condições for falsa, imprime 'Reprovada' no console
    print('Reprovada')