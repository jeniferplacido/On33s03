# Define a variável 'numero' com o valor 8
numero = 8
# Verifica se o resto da divisão de 'numero' por 2 é igual a 0 (o que significa que 'numero' é par)
if numero % 2 == 0:
    # Se 'numero' é par, imprime 'O número é Par' no console
    print ('O número é Par')
# Se o resto da divisão de 'numero' por 2 não é igual a 0 (o que significa que 'numero' é ímpar)
else:
    # Imprime 'O número é Impar' no console
    print ('O número é Impar')