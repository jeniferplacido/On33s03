# Define uma variável 'chovendo' e atribui o valor True a ela
chovendo = True

# Verifica se 'chovendo' é False (ou seja, se não está chovendo)
if not chovendo:
    # Se 'chovendo' for False, imprime 'Você pode sair de casa sem guarda-chuva!' no console
    print('Você pode sair de casa sem guarda-chuva!')
else:
    # Se 'chovendo' for True, imprime 'Está chovendo, leve um guarda chuva!' no console
    print('Está chovendo, leve um guarda chuva!')